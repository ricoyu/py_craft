my_list = [1, 2, 3, 4, 2, 2, 5, 2]

# 使用 count() 方法统计元素 2 出现的次数
count = my_list.count(2)
print(count) # 输出：4
print(f"元素2出现的次数是 {count}")

# 定义一个包含字符串的列表
fruits = ["apple", "banana", "orange", "banana", "apple", "grape"]

# 统计 "apple" 出现的次数
apple_count = fruits.count("apple")

# 打印结果
print(f"苹果出现的次数是: {apple_count}")